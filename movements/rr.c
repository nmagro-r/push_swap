#include "pushswap.h"

void	rr(t_list **a, t_list **b)
{
	if (*b == NULL || (*b)->next == NULL)
		return ;
	if (*a == NULL || (*a)->next == NULL)
		return ;
	ra(a, 1);
	rb(b, 1);
}