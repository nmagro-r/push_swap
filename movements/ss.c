#include "pushswap.h"

void	ss(t_list **a, t_list **b)
{
	if (*b == NULL || (*b)->next == NULL)
		return ;
	if (*a == NULL || (*a)->next == NULL)
		return ;
	sa(a, 1);
	sb(b, 1);
}